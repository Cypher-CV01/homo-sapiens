package com.hominideos.homo_sapiens.model;

public class Homo {

	private long cmCubicoCranio;
	private long cmBraco;
	private long peso;

	public long getCmCubicoCranio() {
		return cmCubicoCranio;
	}

	public void setCmCubicoCranio(long cmCubicoCranio) {
		this.cmCubicoCranio = cmCubicoCranio;
	}

	public long getCmBraco() {
		return cmBraco;
	}

	public void setCmBraco(long cmBraco) {
		this.cmBraco = cmBraco;
	}

	public long getPeso() {
		return peso;
	}

	public void setPeso(long peso) {
		this.peso = peso;
	}

}
